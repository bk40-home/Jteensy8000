/*
 * TUS_Presets.h  –  The Usual Suspects (JE-8086) JP-8000 performance bank
 *
 * Full CC-array format: each preset stores all 128 CC values directly.
 * No transform or field mapping required at load time — the loader
 * simply iterates data[0..127] and sends each non-zero CC.
 *
 * Stored in PROGMEM (Teensy 4.1 flash).
 *
 * To add a new preset:
 *   1. Export from the HTML editor as PATCH:cc,val; text
 *   2. Convert to hex array (use the Python script in /tools/)
 *   3. Append to kTUS_Patches[] below
 *   4. Increment kTUS_COUNT
 *
 * Data layout:
 *   data[n] = CC value for CC number n  (0-127)
 *   Index 0 is CC 0, index 127 is CC 127.
 */

